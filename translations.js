const LANGUAGE_STORAGE_KEY = "hytaleguard_language";
let currentLanguage = detectInitialLanguage();

function detectInitialLanguage() {
    const stored = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (stored && TRANSLATIONS[stored]) {
        return stored;
    }

    const browserLanguage = navigator.language || "pt-BR";
    if (browserLanguage.startsWith("es")) {
        return "es";
    }
    if (browserLanguage.startsWith("en")) {
        return "en";
    }
    return "pt-BR";
}

function t(key) {
    return TRANSLATIONS[currentLanguage]?.[key] || TRANSLATIONS["pt-BR"]?.[key] || key;
}

function setLanguage(language) {
    if (!TRANSLATIONS[language]) {
        return;
    }

    currentLanguage = language;
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    applyTranslations();
    document.dispatchEvent(new CustomEvent("languagechange", { detail: { language } }));
}

function applyTranslations() {
    document.documentElement.lang = currentLanguage;

    const selector = document.getElementById("languageSelect");
    if (selector) {
        selector.value = currentLanguage;
    }

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.getAttribute("data-i18n");
        element.textContent = t(key);
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        const key = element.getAttribute("data-i18n-placeholder");
        element.setAttribute("placeholder", t(key));
    });
}

document.addEventListener("DOMContentLoaded", () => {
    applyTranslations();
    const selector = document.getElementById("languageSelect");
    if (selector) {
        selector.addEventListener("change", (event) => setLanguage(event.target.value));
    }
});
